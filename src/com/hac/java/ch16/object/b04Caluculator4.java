package com.hac.java.ch16.object;

class Calculator{
	int left, right;

	public void setOprands(int left, int right){
		System.out.println("aaaa ::" + this);
	    this.left = left;		//this 클래스를 통해서 만들어진 인스턴스 자신을 가리킴
	    this.right = right;
	}
	  
	public void sum(){
	    System.out.println(this.left+this.right);
	}
	  
	public void avg(){
	    System.out.println((this.left+this.right)/2);
	}
}


public class b04Caluculator4 {
      
    public static void main(String[] args) {
          
        Calculator c1 = new Calculator(); 	// Calculator객체 생성  c1 인스턴스 - 구체적인 객체
        c1.setOprands(10, 20);
        c1.sum();       
        c1.avg();       
          
        Calculator c2 = new Calculator();	// Calculator 데이터타입 컨테이너 를 c2에 
        c2.setOprands(20, 40);
        c2.sum();       
        c2.avg();
    }

}